import fs from 'node:fs/promises'
import path from 'node:path'
import { pathToFileURL } from 'node:url'
import { dataDir, ensureBlogDirs } from './lib/content.mjs'
import { generateWithImageProvider } from './lib/image-providers.mjs'

function has(name) {
    return Boolean(process.env[name] && String(process.env[name]).trim())
}

function providerReport() {
    const provider = process.env.IMAGE_PROVIDER || 'local_comfyui'
    const missing = []
    const warnings = []

    if (provider === 'replicate') {
        return {
            provider,
            configured: false,
            productionReady: false,
            missing: ['IMAGE_PROVIDER=nvidia_flux'],
            warnings: ['Replicate has been removed from this project. Use NVIDIA FLUX only.'],
        }
    }

    if (provider === 'nvidia_flux' || provider === 'nvidia') {
        if (!has('NVIDIA_API_KEY') && !has('NVIDIA_NIM_API_KEY')) missing.push('NVIDIA_API_KEY')
        if (!has('NVIDIA_FLUX_URL')) missing.push('NVIDIA_FLUX_URL')
        if (!has('NVIDIA_FLUX_MODEL')) warnings.push('NVIDIA_FLUX_MODEL is missing; the default FLUX.1-schnell model will be used.')
        if (!has('NVIDIA_IMAGE_SIZE')) warnings.push('NVIDIA_IMAGE_SIZE is missing; 1200x675 intent will be used.')
        if (!has('NVIDIA_FLUX_TIMEOUT_MS')) warnings.push('NVIDIA_FLUX_TIMEOUT_MS is missing; 120000ms will be used.')
    } else if (provider === 'local_comfyui') {
        if (!has('COMFYUI_URL')) missing.push('COMFYUI_URL')
        if (!has('COMFYUI_WORKFLOW_PATH')) missing.push('COMFYUI_WORKFLOW_PATH')
    } else if (provider === 'gpt_image') {
        if (!has('OPENAI_API_KEY')) missing.push('OPENAI_API_KEY')
        if (process.env.USE_GPT_IMAGE !== 'true') warnings.push('Set USE_GPT_IMAGE=true when IMAGE_PROVIDER=gpt_image.')
    } else {
        missing.push(`Unsupported IMAGE_PROVIDER=${provider}`)
    }

    return {
        provider,
        configured: missing.length === 0,
        productionReady: missing.length === 0,
        missing,
        warnings,
    }
}

async function liveNvidiaCheck() {
    const slug = 'nvidia-flux-health-check'
    const output = path.join(dataDir, `${slug}.png`)
    const article = {
        frontmatter: {
            title: 'AI automation workflow image health check',
            slug,
            category: 'AI Automation',
            targetKeyword: 'AI automation',
            contentPersona: 'Hybrid',
            businessFunction: 'Operations',
        },
        imagePrompt: 'A realistic business automation dashboard scene with AI workflow nodes and warm orange accents',
    }
    const result = await generateWithImageProvider({ article, output })
    await fs.rm(output, { force: true })
    return {
        ok: Boolean(result?.path || result?.queued),
        provider: result?.provider || process.env.IMAGE_PROVIDER || 'unknown',
        width: result?.width,
        height: result?.height,
        model: result?.model,
    }
}

export async function validateImageConfig({ live = process.argv.includes('--live') } = {}) {
    await ensureBlogDirs()
    const report = {
        ok: true,
        generatedAt: new Date().toISOString(),
        ...providerReport(),
        buildSafe: true,
        buildSafetyNote: 'npm run build does not call image generation. Images are generated by blog:image or blog:pipeline.',
    }

    if (!report.configured) report.ok = false

    if (live && report.configured && (report.provider === 'nvidia_flux' || report.provider === 'nvidia')) {
        try {
            report.liveCheck = await liveNvidiaCheck()
        } catch (error) {
            report.ok = false
            report.liveCheck = { ok: false, error: error.message }
        }
    }

    await fs.writeFile(path.join(dataDir, 'image-config-report.json'), `${JSON.stringify(report, null, 2)}\n`)
    console.log(JSON.stringify(report, null, 2))
    if (!report.ok) process.exitCode = 2
    return report
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
    validateImageConfig().catch((error) => {
        console.error(error)
        process.exit(1)
    })
}
