import fs from 'node:fs/promises'
import path from 'node:path'
import { pathToFileURL } from 'node:url'
import { dataDir, ensureBlogDirs } from './lib/content.mjs'
import { generateWithImageProvider } from './lib/image-providers.mjs'

function has(name) {
    return Boolean(process.env[name] && String(process.env[name]).trim())
}

function providerReport() {
    const provider = process.env.IMAGE_PROVIDER || 'stable_diffusion'
    const missing = []
    const warnings = []

    if (!has('NVIDIA_API_KEY') && !has('NVIDIA_NIM_API_KEY')) missing.push('NVIDIA_API_KEY')
    if (!has('NVIDIA_SD_URL') && !has('NVIDIA_FLUX_URL')) warnings.push('NVIDIA_SD_URL not set; using default Stable Diffusion 3.5 Large endpoint.')
    if (!has('NVIDIA_IMAGE_SIZE')) warnings.push('NVIDIA_IMAGE_SIZE not set; 1024x576 will be used.')

    return {
        provider: provider,
        model: process.env.NVIDIA_SD_MODEL || 'stabilityai/stable-diffusion-3-5-large',
        configured: missing.length === 0,
        productionReady: missing.length === 0,
        missing,
        warnings,
    }
}

async function liveNvidiaCheck() {
    const slug = 'nvidia-flux-health-check'
    const output = path.join(dataDir, `${slug}.png`)
    const article = {
        frontmatter: {
            title: 'AI automation workflow image health check',
            slug,
            category: 'AI Automation',
            targetKeyword: 'AI automation',
            contentPersona: 'Hybrid',
            businessFunction: 'Operations',
        },
        imagePrompt: 'A realistic business automation dashboard scene with AI workflow nodes and warm orange accents',
    }
    const result = await generateWithImageProvider({ article, output })
    await fs.rm(output, { force: true })
    return {
        ok: Boolean(result?.path || result?.queued),
        provider: result?.provider || process.env.IMAGE_PROVIDER || 'unknown',
        width: result?.width,
        height: result?.height,
        model: result?.model,
    }
}

export async function validateImageConfig({ live = process.argv.includes('--live') } = {}) {
    await ensureBlogDirs()
    const report = {
        ok: true,
        generatedAt: new Date().toISOString(),
        ...providerReport(),
        buildSafe: true,
        buildSafetyNote: 'npm run build does not call image generation. Images are generated by blog:image or blog:pipeline.',
    }

    if (!report.configured) report.ok = false

    if (live && report.configured && (report.provider === 'nvidia_flux' || report.provider === 'nvidia')) {
        try {
            report.liveCheck = await liveNvidiaCheck()
        } catch (error) {
            report.ok = false
            report.liveCheck = { ok: false, error: error.message }
        }
    }

    await fs.writeFile(path.join(dataDir, 'image-config-report.json'), `${JSON.stringify(report, null, 2)}\n`)
    console.log(JSON.stringify(report, null, 2))
    return report
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
    validateImageConfig().catch((error) => {
        console.error(error)
        process.exit(1)
    })
}
